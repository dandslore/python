import math

numero = input('Digite um número:')
dobro = int(numero) * 2
triplo = int(numero) * 3
raiz_quadrada = math.sqrt(int(numero))
print('O dobro de {} vale {}.'.format(numero, dobro))
print('O triplo de {} vale {}.'.format(numero, triplo))
print('A raiz quadrada de {} é igual a {}'.format(numero, raiz_quadrada))
